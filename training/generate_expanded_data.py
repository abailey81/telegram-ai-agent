def naturalize(text: str) -> str:
    """Apply random texting transformations to make text sound natural."""
    r = random.random()
    if r < 0.3:
        text = text.lower()
    elif r < 0.35:
        text = text.upper()
    # Random trailing punctuation
    r2 = random.random()
    if r2 < 0.15 and not text.endswith((".", "!", "?", "...")):
        text += random.choice([".", "!", "!!", "...", "???", " lol", " haha", " tbh"])
    # Random texting abbreviation insertion
    r3 = random.random()
    if r3 < 0.15:
        subs = {"you": "u", "your": "ur", "are": "r", "to": "2",
                "for": "4", "tonight": "2nite", "because": "bc",
                "before": "b4", "tomorrow": "tmrw", "something": "smth",
                "someone": "sm1", "probably": "prolly", "though": "tho",
                "about": "abt", "people": "ppl", "really": "rly",
                "with": "w", "without": "w/o"}
        for full, short in subs.items():
            if full in text.lower() and random.random() < 0.4:
                text = text.replace(full, short, 1)
    return text


def gen_variations(examples: List[str], label: str, target: int) -> List[Tuple[str, str]]:
    """Generate target count of unique (text, label) pairs from seed examples."""
    data = []
    seen = set()

    # First pass: add all raw examples
    for ex in examples:
        ex_clean = ex.strip()
        if ex_clean and ex_clean not in seen:
            data.append((ex_clean, label))
            seen.add(ex_clean)

    # Second pass: add naturalized variations until target is reached
    attempts = 0
    while len(data) < target and attempts < target * 5:
        base = random.choice(examples).strip()
        varied = naturalize(base)
        if varied not in seen:
            data.append((varied, label))
            seen.add(varied)
        attempts += 1

    # Third pass: light augmentation (word order, extra words, repetitions)
    fillers = ["honestly", "ngl", "tbh", "fr", "lowkey", "highkey",
               "literally", "like", "actually", "bruh", "bro", "dude",
               "ugh", "smh", "istg", "imo", "idk man", "lol", "haha"]
    while len(data) < target and attempts < target * 10:
        base = random.choice(examples).strip()
        filler = random.choice(fillers)
        r = random.random()
        if r < 0.35:
            varied = f"{filler} {base}"
        elif r < 0.7:
            varied = f"{base} {filler}"
        else:
            # Add minor emoji (rare, ~30% of this branch)
            if random.random() < 0.3:
                emojis_sparse = ["❤️", "😊", "😢", "😤", "😂", "🔥", "💀", "🙄"]
                varied = f"{base} {random.choice(emojis_sparse)}"
            else:
                varied = f"{base} {random.choice(fillers)}"
        if varied not in seen:
            data.append((varied, label))
            seen.add(varied)
        attempts += 1

    return data[:target]


