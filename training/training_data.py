def get_data_stats() -> Dict[str, Any]:
    """Get statistics about the training data."""
    all_data = get_all_data()
    stats = {}

    for name, data in all_data.items():
        labels = [label for _, label in data]
        unique_labels = set(labels)
        label_counts = {l: labels.count(l) for l in sorted(unique_labels)}
        stats[name] = {
            "total_examples": len(data),
            "num_classes": len(unique_labels),
            "classes": sorted(unique_labels),
            "class_distribution": label_counts,
            "min_per_class": min(label_counts.values()),
            "max_per_class": max(label_counts.values()),
        }

    stats["total_examples_all"] = sum(len(d) for d in all_data.values())
    return stats


