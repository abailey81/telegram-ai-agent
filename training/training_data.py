def _merge_and_dedupe(
    base: List[Tuple[str, str]], extra: List[Tuple[str, str]]
) -> List[Tuple[str, str]]:
    """Merge two datasets, removing exact duplicates while preserving order."""
    seen = set()
    merged = []
    for item in base + extra:
        if item not in seen:
            seen.add(item)
            merged.append(item)
    return merged


